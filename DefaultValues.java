package closedLabBook;

public class DefaultValues {
	static byte value_byte;
	static short value_short;
	static int value_int;
	static long value_long;
	static float value_float;
	static double value_double;
	static String value_string;
	static boolean value_bool;
	public static void main(String[] args) {
		System.out.println("Default values of primitive data types are...");
		System.out.println("byte="+value_byte);
		System.out.println("short="+value_short);
		System.out.println("int="+value_int);
		System.out.println("long="+value_long);
		System.out.println("float="+value_float);
		System.out.println("double="+value_double);
		System.out.println("string="+value_string);
		System.out.println("boolean="+value_bool);

	}
	

}
